int	ft_is_prime(int nb)
{
	int	index;

	if (nb <= 1)
		return (0);
	index = 2;
	while (index <= (nb / 2))
	{
		if (nb % index == 0)
			return (0);
		index ++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	nb++;
	if (nb < 2)
		return (2);
	while (1)
	{
		if (ft_is_prime(nb))
			return (nb);
		nb++;
	}
}
